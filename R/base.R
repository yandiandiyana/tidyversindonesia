# ==========================================
# IndonesiaRaya Base Function Translations
# ==========================================

# head() -> awal()
awal <- function(x, n = 6) {
  head(x, n)
}

# tail() -> akhir()
akhir <- function(x, n = 6) {
  tail(x, n)
}

# str() -> struktur()
struktur <- function(x) {
  str(x)
}

# summary() -> ringkasan()
ringkasan <- function(x) {
  summary(x)
}

# dim() -> dimensi()
dimensi <- function(x) {
  dim(x)
}

# class() -> kelas()
kelas <- function(x) {
  class(x)
}

# is.na() -> hilang()
hilang <- function(x) {
  is.na(x)
}
